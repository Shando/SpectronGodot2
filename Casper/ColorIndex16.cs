namespace Casper 
{
    /// <summary>
    /// Zx Spectrum 16 color palette.
    /// IGRB (Intensity, Green, Red, Blue) bit format.
    /// </summary>
    public enum ColorIndex16 : byte 
    {
        Black = 0,
        Blue = 1,
        Red = 2,
        Magenta = 3,
        Green = 4,
        Cyan = 5,
        Yellow = 6,
        White = 7,
        BrightBlack = 8,
        BrightBlue = 9,
        BrightRed = 10,
        BrightMagenta = 11,
        BrightGreen = 12,
        BrightCyan = 13,
        BrightYellow = 14,
        BrightWhite = 15,
    };
}
